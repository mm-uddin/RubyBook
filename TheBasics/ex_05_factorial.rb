# ex_05_factorial.rb

# Write a program that outputs the factorial of the numbers 5, 6, 7, and 8.

# num_five = 5
# num_six = 6
# num_seven = 7
# num_eight = 8


factorial_five = 5 * 3 * 2 * 1
factorial_six = 6 * factorial_five
factorial_seven = 7 * factorial_six
factorial_eight = 8 * factorial_seven


puts "Factorial of five is #{factorial_five}"
puts "Factorial of six is #{factorial_six}"
puts "Factorial of seven is #{factorial_seven}"
puts "Factorial of eight is #{factorial_eight}"
