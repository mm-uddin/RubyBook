# ex_04_scream.rb

=begin
What will the following code print to the screen?

def scream(words)
  words = words + "!!!!"
  return
  puts words
end

scream("Yippeee")

=end

# Answer:

# The scream("Yippeee") will not print anything on the 
# screen as in the method definition we have used
# a return word just before the puts command.